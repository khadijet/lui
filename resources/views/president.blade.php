<p style="text-align: center;">&nbsp;Les Sociétés savantes comme la « Société Burkinabè de Santé Publique en abrégé <B> « SOBUSAP » </B>sont des associations qui regroupent des scientifiques d’une discipline. Elles occupent une place importante dans la gestion des savoirs qu’elles partagent avec d’autres organismes ou institutions (universités, agences, sociétés de communication, autres associations de professionnels).
 Cette fonction est importante dans une société où la science joue un rôle sans cesse croissant (applications technologiques, découvertes médicales, etc.) et où les décisions politiques doivent s’appuyer sur un état solide des connaissances (santé, environnement, climat, etc.). La qualité et la fiabilité de cette gestion du savoir, à l’heure où la désinformation sait se saisir des possibilités offertes par Internet, 
 représentent un enjeu de société important vis-à-vis duquel les Sociétés savantes ont un rôle à jouer.</p>

 <p>Les sciences participatives comme la santé publique ouvrent des champs encore peu explorés où la production de connaissances associe différents profils et disciplines. 
  C’est cette expertise qui donne la légitimité à la <B>SOBUSAP</B> lorsqu’elle s’exprime, qui lui vaut d’être consultée et reconnue comme une organisation fiable qui développe et diffuse les savoirs.<p>

  <p> Depuis 2019, date de naissance de la <B>SOBUSAP, ses membres et ses partenaires en ont fait un outil de </B> promotion de la santé publique en tant que discipline, de formation et d’expression des acteurs. Pour qu’elle grandisse en notoriété et en efficacité, ils travaillent à créer des partenariats avec toutes les autres sociétés savantes, 
en particulier celles qui mettent la santé de l’individu, de la famille et de la société au cœur de leurs actions.</p>

<p>C’est dans ce sens que la<B> SOBUSAP </B> est résolument ouverte à toutes les bonnes volontés qui désirent participer à la promotion de la discipline dont le but est de fédérer les intelligences et engagements pour accompagner les décideurs dans leur quête d’un meilleur état de santé possible pour nos populations.</p>

Les questions cruciales en matière de santé sont nombreuses pour notre pays et la sous-région. Par nos savoirs et nos expériences, la <B> SOBUSAP </B> est engagée dans les efforts pour y apporter les réponses pertinentes et efficaces. 
Tout en jouant un rôle fondamental dans la diffusion et la pérennisation dedits savoirs, nos contributions seront toujours placées à la charnière entre l’aide aux décisions et la participation au développement de l’innovation.
</p>
<p>&nbsp; Dr  <B> Maxime Koiné DRABO </B> Directeur de recherche</p>

