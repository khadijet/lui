<p style="text-align: center;">&nbsp;La Société burkinabé de santé publique offre un espace ouvert à de nombreuses disciplines pour une réflexion collective et interprofessionnelle. Cette réflexion repose en particulier sur l’analyse critique
   des faits scientifiques et des pratiques professionnelles.
 Elle permet de formuler des propositions à l’intention des décideurs et, via les médias, d’éclairer l’opinion publique sur les enjeux des &nbsp;<a href="https://fr.wikipedia.org/wiki/Politique_de_sant%C3%A9" class="link-success  link-underline link-underline-opacity-0">politique de santé</a>,
  les forces et les faiblesses de leur mise en œuvre.</p>

  <p style="text-align: center;"> &nbsp;La Revue <i>Santé publique</i> est une revue scientifique, francophone. Elle publie des articles de recherches originales, des analyses d’actions et programmes et des opinions.
 Bimestrielle, elle est indexée dans de nombreuses bases de données (<a href="https://fr.wikipedia.org/wiki/MEDLINE" class="link-success  link-underline link-underline-opacity-0">Medline</a> Excerpta Medica/<a href="https://fr.wikipedia.org/wiki/Embase" class="link-success  link-underline link-underline-opacity-0">EMBASE</a>, PASCAL, SCOPUS et <a href="https://fr.wikipedia.org/wiki/Science_Citation_Index" class="link-success  link-underline link-underline-opacity-0">Science Citation Index</a>, <a href="https://fr.wikipedia.org/wiki/Journal_Citation_Reports" class="link-success  link-underline link-underline-opacity-0">Journal Citation Report</a>/Science Edition).</p>

